        long l1 = 256458L;
        long l2 = 5568L;
        System.out.println("Addition: " + l1 + " + " + l2 + " = " + (l1 + l2));
        System.out.println("Subtraction: " + l1 + " - " + l2 + " = " + (l1 - l2));
        System.out.println("Multiplication: " + l1 + " x " + l2 + " = " + (l1 * l2));
