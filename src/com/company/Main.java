        System.out.println("FLOAT TYPE");
        float f1 = 542.254f;
        float f2 = 264.6558f;
        System.out.println("Addition: " + f1 + " + " + f2 + " = " + (f1 + f2));
        System.out.println("Subtraction: " + f1 + " - " + f2 + " = " + (f1 - f2));
        System.out.println("Multiplication: " + f1 + " x " + f2 + " = " + (f1 * f2));
        System.out.println("Division: " + f1 + " / " + f2 + " = " + (f1 / f2));
